// 1. Write a program to multiply 2 integers a & b (take input from user).
// Constraint: 1 ≤ 𝑎, 𝑏 ≤ 10 .
// 1000
// Sample Runtime Environment:

#include <iostream>
#include <string>
using namespace std;

int main()
{
    string a, b;
    cout << "Enter a: " << endl;
    cin >> a;
    cout << "Enter b: " << endl;
    cin >> b;
    int n = a.length();
    int m = b.length();
    int *c = new int[n + m]();
    for (int i = m - 1; i >= 0; i--)
    {
        for (int j = n - 1; j >= 0; j--)
        {
            int x = (a[j] - '0') * (b[i] - '0');
            int p1 = i + j;
            int p2 = i + j + 1;
            int sum = x + c[p2]; // sum of the curr product and the prev carry
            c[p1] += sum / 10;   // carry
            c[p2] = sum % 10;    // remainder
        }
    }
    int i = 0;
    cout << "Multiplication Result: " << endl;
    while (c[i] == 0)
    {
        i++;
    }
    if (i == n + m)
        cout << 0 << endl;
    while (i < n + m)
    {
        cout << c[i];
        i++;
    }
    return 0;
}

